        public static let roundedRectangleHeight = CGFloat(50.0) // Die Höhe des abgerundeten Rechtecks für den Trip-Button
        public static let roundedRectangleCornerRadius = CGFloat(25.0) // Der Radius der abgerundeten Ecken des Rechtecks für den Trip-Button
        public static let imageWidth = CGFloat(500.0) // Die Breite des Bildes für den Trip-Button
        public static let imageHeight = CGFloat(150.0) // Die Höhe des Bildes für den Trip-Button
        public static let textSize = CGFloat(30.0) // Die Textgröße für den Trip-Button
    }
}
